package com.example.symptologger;

public class ConcernMap {
    private String concernTitle;
    private String concernDate;
    private String concernDescription;
    private String userName;

    public String getConcernTitle(){
        return concernTitle;
    }

    public String getConcernDate(){
        return concernDate;
    }

    public String getConcernDescription(){
        return concernDescription;
    }

    public String getUserName(){
        return userName;
    }
}
